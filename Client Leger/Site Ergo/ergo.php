      <h2 class="p1">Définition de l'ergothérapie</h2>
      <br>
      <br>
      <h1 class="p2">
      « L'ergothérapie est une profession de santé, paramédicale, évaluant et traitant les personnes au moyen d'activités significatives pour préserver et développer leur indépendance (capacités à faire) et leur autonomie (capacités à gérer et à décider) dans leur environnement quotidien et social.
      </h1>
      <br>
      <br>
      <h1 class="p2">
      L'ergothérapie se pratique au travers de la <b>rééducation</b> (cognitive, motrice, sensitive, sensorielle, ...) ou de la <b>réadaptation</b> (aides techniques, compensations motrices ou cognitives, aménagements de domicile, domotique, ...) par et pour l'activité (du Grec, Ergon).
      </h1>
      <br>
      <br>
      <h1 class="p2">
      Sa fonction consiste à permettre aux patients <b>l'acquisition d'une plus grande autonomie</b> en recourant à différentes techniques et activités. Toujours dans une optique d'autonomie, il donne des conseils à la personne et à son entourage en vue d'une meilleure intégration dans son environnement matériel et humain.
      </h1>
      <br>
      <br>
      <h1 class="p2">
      L'ergothérapeute intervient autant sur le nourrisson, l'enfant, ou l'adulte. Il exerce sa profession sur prescription médicale, en centre, en service commercial, dans les assurances, les instances administratives ou en libéral. »
      </h1>
      <br>
      <h1 class="p2">
      Source <i>Synfel</i>
      </h1>
